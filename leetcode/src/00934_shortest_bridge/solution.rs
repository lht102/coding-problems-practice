use std::collections::VecDeque;

struct Solution;

impl Solution {
    pub fn shortest_bridge(grid: Vec<Vec<i32>>) -> i32 {
        let pos = Self::find_island(&grid);
        if pos.is_none() {
            return -1;
        }
        let (x1, y1) = pos.unwrap();
        let m = grid.len();
        let n = grid[0].len();
        let mut visited = vec![vec![false; n]; m];
        let mut q = VecDeque::new();
        Self::dfs(&grid, &mut visited, x1, y1, &mut q);
        let m = m as i32;
        let n = n as i32;
        let mut level = 0;
        let dirs: &'static [i32] = &[0, 1, 0, -1, 0];
        while !q.is_empty() {
            for _ in 0..q.len() {
                let (ci, cj) = q.pop_front().unwrap();
                for i in 0..4 {
                    let ni = ci as i32 + dirs[i];
                    let nj = cj as i32 + dirs[i + 1];
                    if ni < 0 || ni >= m || nj < 0 || nj >= n {
                        continue;
                    }
                    let ni = ni as usize;
                    let nj = nj as usize;
                    if visited[ni][nj] {
                        continue;
                    }
                    if grid[ni][nj] == 1 {
                        return level;
                    }
                    visited[ni][nj] = true;
                    q.push_back((ni, nj));
                }
            }
            level += 1;
        }
        -1
    }

    fn find_island(grid: &[Vec<i32>]) -> Option<(usize, usize)> {
        for (i, row) in grid.iter().enumerate() {
            for (j, &x) in row.iter().enumerate() {
                if x == 1 {
                    return Some((i, j));
                }
            }
        }
        None
    }

    fn dfs(
        grid: &[Vec<i32>],
        visited: &mut [Vec<bool>],
        i: usize,
        j: usize,
        q: &mut VecDeque<(usize, usize)>,
    ) {
        if visited[i][j] || grid[i][j] != 1 {
            return;
        }
        visited[i][j] = true;
        q.push_back((i, j));
        if i >= 1 {
            Self::dfs(grid, visited, i - 1, j, q);
        }
        if i + 1 < grid.len() {
            Self::dfs(grid, visited, i + 1, j, q);
        }
        if j >= 1 {
            Self::dfs(grid, visited, i, j - 1, q);
        }
        if j + 1 < grid[i].len() {
            Self::dfs(grid, visited, i, j + 1, q);
        }
    }
}

